from bs4 import BeautifulSoup
import requests



def weather_request(message):
    query = message.text.split()
    return False if query[0].lower() != 'weather' else True


headers = {
    'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) '
                  'Chrome/58.0.3029.110 Safari/537.3'
}


def get_weather(city):
    city = city.replace(" ", "+")
    res = requests.get(
        f'https://www.google.com/search?hl=en&q={city}&oq={city}&aqs=chrome.0.35i39l2j0l4j46j69i60.6128j1j7&sourceid=chrome'
        f'&ie=UTF-8',
        headers=headers)
    soup = BeautifulSoup(res.text, 'html.parser')
    location = soup.select('#wob_loc')[0].getText().strip()
    time = soup.select('#wob_dts')[0].getText().strip()
    info = soup.select('#wob_dc')[0].getText().strip()
    weather = soup.select('#wob_tm')[0].getText().strip()
    msg = location + '\n' + time + '\n' + info + '\n' + weather + "Â°C"
    return msg


